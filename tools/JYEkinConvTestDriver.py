'''
this driver will be much simpler than the JYLmaxRcutJointConvTestDriver

In this kind of test, abacus calculation will run in serial from the 
orbital with the lowest kinetic energy cutoff to the highest. If not the
first, the charge density yielded by the previous calculation will be
used as the initial guess of the charge density.
'''

